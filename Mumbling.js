function accum(s) {
  const thing = s.split("");
  const myArray = [];
  for (let i=0;i<thing.length;i++){
    if (i===0){
      let first = thing.slice(i,i+1);
      myArray.push(first.map(x => x.toUpperCase()));
    } else {
      let second = thing.slice(i,i+1);
      myArray.push(second.map(x => x.toUpperCase()+ x.toLowerCase().repeat(i)));
    } 
  }
  return myArray.join('-');
}
